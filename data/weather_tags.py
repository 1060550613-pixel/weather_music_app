# 八、辅助函数：根据天气获取推荐情绪标签
# ============================================================
def get_weather_emotions(weather: str, temp_range: str = None, time_of_day: str = None) -> list:
    """综合天气、温度、时间，返回推荐情绪标签列表"""
    emotions = WEATHER_EMOTION_MAP.get(weather, ["治愈"]).copy()

    if temp_range:
        for tag, info in TEMPERATURE_TAGS.items():
            if info["range"] == temp_range:
                emotions.extend(info["emotions"])
                break

    if time_of_day:
        emotions.extend(TIME_MOOD_MAP.get(time_of_day, []))

    return list(set(emotions))  # 去重


def get_mood_quote(weather: str) -> str:
    """获取当前天气的心情文案"""
    return WEATHER_CONDITIONS.get(weather, "无论什么天气，音乐都在陪你。")
